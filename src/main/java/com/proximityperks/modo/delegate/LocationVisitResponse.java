package com.proximityperks.modo.delegate;

public class LocationVisitResponse {

	private String checkoutCode;
	private String barcodeImageData;

	public String getCheckoutCode() {
		return checkoutCode;
	}

	public void setCheckoutCode(String checkoutCode) {
		this.checkoutCode = checkoutCode;
	}

	public String getBarcodeImageData() {
		return barcodeImageData;
	}

	public void setBarcodeImageData(String barcodeImageData) {
		this.barcodeImageData = barcodeImageData;
	}

}
